#Operadores de Comparação são usados para comparar dois valores ou expressões e retornam um valor booleano (True ou False) com base na comparação.

#Igualdade (==) - saída booleana
x = 5
y = 5  
z = 4 
print(f"Igualdade: {x} == {y} ->", x == y)  # Saída: True
print(f"Igualdade: {x} == {z} ->", x == z)  # Saída: False

#Diferente (!=)
print(f"Diferente: {x} != {z} ->", x != z)  # Saída: True

#Maior que (>)
print(f"Maior que: {x} > {z} ->", x > z)  # Saída: True

#Menor que (<)
print(f"Menor que: {z} < {y} ->", z < y)  # Saída: True

#Maior ou igual a (>=)
print(f"Maior ou igual a: {x} >= {y} ->", x >= y)  # Saída: True

#Menor ou igual a (<=)  
print(f"Menor ou igual a: {z} <= {y} -> {z <= y}")  # Saída: True