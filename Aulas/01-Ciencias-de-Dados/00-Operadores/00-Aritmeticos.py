#Operadores são símbolos que realizam operações em variáveis e valores.

#Adição (+)
a = 10 + 2
print("Adição: 10 + 2 = ", a)  # Saída: 12


#Subtração (-)
b = 10 - 2  
print("Subtração: 10 - 2 = ", b)  # Saída: 8

#Divisão (/)
c = 10 / 2  
print("Divisão: 10 / 2 = ", c)  # Saída: 5.0
#Multiplicação (*)
d = 10 * 2  
print("Multiplicação: 10 * 2 = ", d)  # Saída: 20

#Modulo - resto da divisão  
e = 10 % 3  
print("Módulo: 10 % 3 = ", e)  # Saída: 1
#Exponenciação (**)
f = 2 ** 3  
print("Exponenciação: 2 ** 3 = ", f)  # Saída: 8

